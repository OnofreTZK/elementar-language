#include <stdio.h>

int floatToInt(float a) {
    return (int)a;
}

float intToFloat(int a) {
    return (float)a;
}

int doubleToInt(double a) {
    return (int)a;
}

double intToDouble(int a) {
    return (double)a;
}

double floatToDouble(float a) {
    return (double)a;
}

float doubleToFloat(double a) {
    return (float)a;
}
